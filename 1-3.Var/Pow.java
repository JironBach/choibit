public class Pow {
   public static void main(String[] args){
      System.out.println("整数の演算");
      System.out.println(Math.pow(2, 3));
      System.out.println("-------");

      System.out.println("小数の演算");
      System.out.println(Math.pow(2.2, 3.3));
      System.out.println("-------");
   }

}

